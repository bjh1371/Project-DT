#include "Lifespan.h"

namespace
{
	std::atomic<bool> g_LifeSpan = true;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// \brief 
/// \return bool
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool IsAppRunning()
{
	return g_LifeSpan.load();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// \brief 
/// \return void
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void TerminateApp()
{
	g_LifeSpan.store(false);
}

