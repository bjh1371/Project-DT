class CNoneCopyable
{
public:
	/// \brief 생성자
	CNoneCopyable();
	
	/// \brief 소멸자
	virtual ~CNoneCopyable();

	CNoneCopyable(const CNoneCopyable&) = delete;
	CNoneCopyable& operator = (const CNoneCopyable&) = delete;
};